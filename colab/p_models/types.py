class Terrain:
    """지형 효과 클래스"""
    GRASSY = "Grassy Terrain"    # 풀지형
    ELECTRIC = "Electric Terrain" # 전기지형
    PSYCHIC = "Psychic Terrain"   # 에스퍼지형
    MISTY = "Misty Terrain"       # 페어리지형

def get_type_name(type_id):
    """타입 ID에 해당하는 이름 반환"""
    for name, id in TYPE_NAMES.items():
        if id == type_id:
            return name
    return "Unknown" 